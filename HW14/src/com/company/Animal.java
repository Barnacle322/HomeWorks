package com.company;

public class Animal {
    int legs;
    private String livesOn;

    public void setLegs(int legs) {
        this.legs = legs;
    }

    public void setLivesOn(String livesOn) {
        this.livesOn = livesOn;
    }

    public int getLegs() {
        return legs;
    }

    public String getLivesOn() {
        return livesOn;
    }
}
